CREATE TABLE iiiItems (
    id INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
    created DATETIME DEFAULT CURRENT_TIMESTAMP,
    item_url VARCHAR NOT NULL
);

CREATE TABLE log (
    id INTEGER PRIMARY KEY AUTOINCREMENT NOT NULL,
    created DATETIME DEFAULT CURRENT_TIMESTAMP,
    item INTEGER,
    last_check_out DATE,
    total_checkouts INTEGER NOT NULL,
    FOREIGN KEY (item) REFERENCES iiiItems (id)
) ;
